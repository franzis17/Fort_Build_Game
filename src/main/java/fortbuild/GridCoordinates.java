package fortbuild;

public class GridCoordinates
{
    private int x;
    private int y;
    
    public GridCoordinates(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
    
    public int getX()
    {
        return x;
    }
    
    public int getY()
    {
        return y;
    }
}
